# CONCEPT: printing data
# You can use the print() function to display most kinds of data. We'll be
# using this a lot
print('Hello world')
print(5)

# CONCEPT: variables and value assignment
# We use two variables, x and y. Variables get values assigned using the = 
# character
x = 'Hello world'
y = 5
print(x)
print(y)

# Other simple examples
a = 3
b = 2
print(a - b)
print(a + b)
print(a * b)